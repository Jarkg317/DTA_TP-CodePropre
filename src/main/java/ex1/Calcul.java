package ex1;


/**
 * La classe calcul permets de faire des additions et des soustraction
 *
 */
public class Calcul {

	/**
	 * Cette methode additionne deux entiers mis en parametre 
	 * et qui retourne aussi un entier. 
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * Cette methode soustrait deux entiers mis en parametre 
	 * et renvoir un entier. 
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
